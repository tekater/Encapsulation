public class Point2D {
    float x = 0;
    float y = 0;
    // Setter
    public void setX(float x) {
        this.x = x;
    }
    public void setY(float y) {
        this.y = y;
    }
    // Getter
    public float getX() {
        return this.x;
    }
    public float getY() {
        return this.y;
    }

}
